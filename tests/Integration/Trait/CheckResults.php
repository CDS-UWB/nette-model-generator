            namespace App\Model\Generated\Rows;

            use Nette\Database\Table\ActiveRow;

            abstract class TestBasicColumnsActiveRowBase extends ActiveRow
            {
                public int $id {
                    get => $this['id'];
                }

                public string $textValue {
                    get => $this['text_value'];
                }

                public string|null $optionalText {
                    get => $this['optional_text'];
                }

                public bool|null $boolValue {
                    get => $this['bool_value'] !== null ? (bool) $this['bool_value'] : null;
                }

                public \DateTime|null $createdAt {
                    get => $this['created_at'];
                }
            }

            PHP;

