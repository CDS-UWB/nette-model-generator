    /**
     * @param array{
     *     basic: array{id: string, text_value: string, optional_text: string, bool_value: string, created_at: string},
     *     date_time: array{id: string, date_value: string, time_value: string, datetime_value: string, timestamp_value: string},
     *     enum: array{id: string, status: string, priority: string},
     *     json_and_binary: array{id: string, json_value: string, blob_value: string, long_text_value: string},
     *     number: array{id: string, tiny_value: string, small_value: string, int_value: string, big_value: string, decimal_value: string, float_value: string, double_value: string}
     * } $columnTypes
     */
    public function checkColumns(string $outDir, array $columnTypes, string $path = 'App/Model/Generated/Columns'): void
    {
        $columnsDir = $outDir . '/' . $path;

        $this->checkBasicColumns($columnsDir . '/TestBasicColumns.php', $columnTypes['basic']);
        $this->checkDateTimeColumns($columnsDir . '/TestDateTimeColumns.php', $columnTypes['date_time']);
        $this->checkEnumColumns($columnsDir . '/TestEnumColumns.php', $columnTypes['enum']);
        $this->checkJsonAndBinaryColumns($columnsDir . '/TestJsonAndBinaryColumns.php', $columnTypes['json_and_binary']);
        $this->checkNumberColumns($columnsDir . '/TestNumberColumns.php', $columnTypes['number']);
    }

    /**
     * @param string $enumPrefix In MariaDB, the types are per table, so the table name is used as prefix.
     *                           In PostgreSQL, the types are global, so there is no prefix.
     */
    public function checkEnums(string $outDir, string $path = 'App/Model/Generated/Enums', string $enumPrefix = 'TestEnum'): void
    {
        $enumsDir = $outDir . '/' . $path;

        $this->checkPriorityEnum($enumsDir . "/{$enumPrefix}ColumnsPriority.php", $enumPrefix);
        $this->checkStatusEnum($enumsDir . "/{$enumPrefix}ColumnsStatus.php", $enumPrefix);
    }

    public function checkManagersBase(string $outDir, string $schema = '', string $path = 'App/Model/Generated/Managers'): void
    {
        $managersDir = $outDir . '/' . $path;

        $this->checkBasicColumnsManagerBase($managersDir . '/TestBasicColumnsManagerBase.php', $schema);
        $this->checkDateTimeColumnsManagerBase($managersDir . '/TestDateTimeColumnsManagerBase.php', $schema);
        $this->checkEnumColumnsManagerBase($managersDir . '/TestEnumColumnsManagerBase.php', $schema);
        $this->checkJsonAndBinaryColumnsManagerBase($managersDir . '/TestJsonAndBinaryColumnsManagerBase.php', $schema);
        $this->checkNumberColumnsManagerBase($managersDir . '/TestNumberColumnsManagerBase.php', $schema);
    }

    public function checkRowsBase(string $outDir, string $path = 'App/Model/Generated/Rows'): void
    {
        $rowsDir = $outDir . '/' . $path;

        $this->checkBasicColumnsRowBase($rowsDir . '/TestBasicColumnsActiveRowBase.php');
        $this->checkDateTimeColumnsRowBase($rowsDir . '/TestDateTimeColumnsActiveRowBase.php');
        $this->checkEnumColumnsRowBase($rowsDir . '/TestEnumColumnsActiveRowBase.php');
        $this->checkJsonAndBinaryColumnsRowBase($rowsDir . '/TestJsonAndBinaryColumnsActiveRowBase.php');
        $this->checkNumberColumnsRowBase($rowsDir . '/TestNumberColumnsActiveRowBase.php');
    }

    /**
     * @param array{id: string, text_value: string, optional_text: string, bool_value: string, created_at: string} $types
     */
    private function checkBasicColumns(string $filePath, array $types): void
    {
        $php = <<<PHP
            <?php

            /**
             * This file is automatically generated using `cds/nette-model-generator`.
             *
             * Do not edit!
             */

            declare(strict_types=1);

            namespace App\\Model\\Generated\\Columns;

            class TestBasicColumns
            {
                public const Id = 'id';
                public const TextValue = 'text_value';
                public const OptionalText = 'optional_text';
                public const BoolValue = 'bool_value';
                public const CreatedAt = 'created_at';

                /**
                 * Returns an array of column names.
                 *
                 * @return array<string, string>
                 */
                public static function getColumns(): array
                {
                    return [
                        'id' => '{$types['id']}',
                        'text_value' => '{$types['text_value']}',
                        'optional_text' => '{$types['optional_text']}',
                        'bool_value' => '{$types['bool_value']}',
                        'created_at' => '{$types['created_at']}',
                    ];
                }
            }

            PHP;

        $actual = file_get_contents($filePath);
        $this->assertNotFalse($actual);
        $this->assertSame($php, $actual);
    }

